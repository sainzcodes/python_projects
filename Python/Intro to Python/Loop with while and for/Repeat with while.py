'''The simples ooping mechanism in python is while. Assign the value of 1 to count.
The value of the while loop will compare the value of count to 5 and continue if count is 
less or equal to 5. After printing the value of count, it incrementes its value by one 
witht the statement +=1 '''
	
count = 0
while count <= 5:
	print(count)
	count += 1